<?php 

// Operation Addtion (+)
$total = 500;

$fruit = 5000;
$chicken = 10000;
$orangeJuice = 4000;


$total += $fruit;
$total += $chicken;
$total += $orangeJuice;

var_dump($total);

// Operation Subtrction (-)
$total1 = 500;

$fruit = 5000;
$chicken = 10000;
$orangeJuice = 4000;


$total1 -= $fruit;
$total1 -= $chicken;
$total1 -= $orangeJuice;

var_dump($total1);
?>